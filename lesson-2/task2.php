<?php

// Определить сложность следующих алгоритмов. Сколько произойдет итераций?

$n = 100;
$array[] = [];

for ($i = 0; $i < $n; $i++) { // O(N)
    for ($j = 1; $j < $n; $j *= 2) { // O(logN)
        $array[$i][$j] = true;
    }
}
// Сложность => O(N) * O(logN) = O(NlogN);

$n = 100;
$array[] = [];

for ($i = 0; $i < $n; $i += 2) { // O(N/2) = O(N)
    for ($j = $i; $j < $n; $j++) { // O(N)
        $array[$i][$j] = true;
    }
}
// Сложность => O(N) * O(N) = O(N^2);